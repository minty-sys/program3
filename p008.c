//WAP to print ASCII value of character
#include <stdio.h>

int main()
{
    int x;
    char a;
    printf("Enter the character:%c\n");
    scanf("%c", &a);
    x = a;
    printf("ASCII value of character is :%d\n", x);
    return 0;
}

/*OUTPUT:
Enter the character:
A
ASCII value of character is :65
*/
