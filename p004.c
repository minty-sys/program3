//WAP to find square & cube of a number

#include <stdio.h>

int main()
{
    int a;
    printf("Enter a number:\n");
    scanf("%d", &a);
    printf("Square of a number is: %d\n", a * a);
    printf("Cube of a number is: %d\n", a * a * a);
    return 0;
}

/*OUTPUT:
Enter a number:
4
Square of a number is: 16
Cube of a number is: 64
*/